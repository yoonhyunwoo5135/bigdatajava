package array;

public class 복사연습 {

	public static void main(String[] args) {
		int num1 = 300;
		int num2 = 100;
		//num3에 num1값을 복사해 보세요.확인
		int num3 = num1;
		System.out.println(num1);
		System.out.println(num3);
		//num2에 num1값을 복사해 보세요.확인
		num2 = num1;
		System.out.println(num1);
		System.out.println(num2);
	}
}
