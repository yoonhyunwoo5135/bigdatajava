package 반복문;

public class WhileTest1v {

	public static void main(String[] args) {
		// 반복할때 사용 하는 문법 - while, for
				//while(true) {//무한 루프
				//변수는 데이터를 저장하는 공간
				
				int x = 100;//초기값
				int y = 200;
				
				
				while(x < y) {//조건식(x < y)
					System.out.println("환영합니다.");
					x = x + 10;//증감식
					 
					 
				 }//조건의 결과는 항상 boolean 형(논리형)
		
		}

}
